# ================================
# рЃЏрЃЮрЃЌрЃљрЃЏрЃљрЃерЃўрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ
# рЃБрЃќрЃарЃБрЃюрЃЋрЃћрЃџрЃДрЃЮрЃцрЃА рЃарЃЮрЃЏ:
# - рЃўрЃДрЃЮрЃА рЃќрЃБрЃАрЃбрЃљрЃЊ 6 рЃарЃўрЃфрЃ«рЃЋрЃў
# - рЃљрЃа рЃњрЃљрЃюрЃЏрЃћрЃЮрЃарЃЊрЃћрЃА
# ================================
def get_player_numbers():
    numbers = set()  # set рЃЋрЃўрЃДрЃћрЃюрЃћрЃЉрЃЌ рЃЊрЃБрЃЉрЃџрЃўрЃЎрЃљрЃбрЃћрЃЉрЃўрЃА рЃЌрЃљрЃЋрЃўрЃЊрЃљрЃю рЃљрЃАрЃљрЃфрЃўрЃџрЃћрЃЉрЃџрЃљрЃЊ

    while len(numbers) < 6:
        n = int(input(f"рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃарЃўрЃфрЃ«рЃЋрЃў #{len(numbers) + 1} (1-49): "))
        numbers.add(n)

    return list(numbers)


