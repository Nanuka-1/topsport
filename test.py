    level=logging.INFO,              # рЃџрЃЮрЃњрЃўрЃарЃћрЃЉрЃўрЃА рЃЊрЃЮрЃюрЃћ (INFO РђЊ рЃЕрЃЋрЃћрЃБрЃџрЃћрЃЉрЃарЃўрЃЋрЃў рЃЏрЃЮрЃЋрЃџрЃћрЃюрЃћрЃЉрЃў)
    format="%(asctime)s - %(message)s",  # рЃЊрЃарЃЮ + рЃерЃћрЃбрЃДрЃЮрЃЉрЃўрЃюрЃћрЃЉрЃљ
    encoding="utf-8"                 # рЃЦрЃљрЃарЃЌрЃБрЃџрЃў рЃбрЃћрЃЦрЃАрЃбрЃўрЃА рЃАрЃгрЃЮрЃарЃљрЃЊ рЃЕрЃљрЃгрЃћрЃарЃўрЃАрЃЌрЃЋрЃўрЃА
