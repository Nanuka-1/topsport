# $  - рЃАрЃбрЃарЃўрЃюрЃњрЃўрЃА рЃЊрЃљрЃАрЃљрЃАрЃарЃБрЃџрЃў
# рЃљрЃюрЃБ: рЃЏрЃЌрЃџрЃўрЃљрЃюрЃў рЃАрЃљрЃ«рЃћрЃџрЃў рЃБрЃюрЃЊрЃљ рЃўрЃДрЃЮрЃА рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ a-z рЃЊрЃљ рЃАрЃ«рЃЋрЃљ рЃљрЃарЃљрЃцрЃћрЃарЃў
NAME_PATTERN = re.compile(r"^[a-z]+$")  # pattern-рЃА рЃЋрЃљрЃЎрЃЮрЃЏрЃърЃўрЃџрЃўрЃарЃћрЃЉрЃЌ рЃћрЃарЃЌрЃ«рЃћрЃџ, рЃарЃЮрЃЏ рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ рЃўрЃДрЃЮрЃА рЃЏрЃљрЃарЃбрЃўрЃЋрЃў/рЃАрЃгрЃарЃљрЃцрЃў

# =========================================================
# рЃЋрЃљрЃџрЃўрЃЊрЃљрЃфрЃўрЃўрЃА рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ: рЃљрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃА рЃАрЃљрЃ«рЃћрЃџрЃА рЃЊрЃљ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃА
# =========================================================
def validate_name(name: str) -> list[str]:
    # errors - рЃљрЃЦ рЃерЃћрЃЋрЃљрЃњрЃарЃЮрЃЋрЃћрЃЉрЃЌ рЃДрЃЋрЃћрЃџрЃљ рЃърЃарЃЮрЃЉрЃџрЃћрЃЏрЃљрЃА, рЃарЃЮрЃЏ рЃћрЃарЃЌрЃЊрЃарЃЮрЃБрЃџрЃљрЃЊ рЃЋрЃљрЃЕрЃЋрЃћрЃюрЃЮрЃЌ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА
    errors: list[str] = []

    # s - рЃерЃћрЃЏрЃЮрЃАрЃБрЃџрЃў рЃбрЃћрЃЦрЃАрЃбрЃўрЃА "рЃњрЃљрЃгрЃЏрЃћрЃюрЃЊрЃљ": None/рЃфрЃљрЃарЃўрЃћрЃџрЃў рЃЌрЃБ рЃўрЃДрЃЮ, "" рЃњрЃљрЃ«рЃЊрЃћрЃЉрЃљ рЃЊрЃљ рЃќрЃћрЃЊрЃЏрЃћрЃбрЃў space рЃЏрЃЮрЃўрЃерЃџрЃћрЃЉрЃљ
    s = (name or "").strip()

    # рЃЌрЃБ рЃАрЃљрЃЉрЃЮрЃџрЃЮрЃЮрЃЊ рЃфрЃљрЃарЃўрЃћрЃџрЃўрЃљ, рЃърЃўрЃарЃЊрЃљрЃърЃўрЃа рЃЋрЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃЌ 1 рЃерЃћрЃфрЃЊрЃЮрЃЏрЃљрЃА (рЃарЃљрЃЊрЃњрЃљрЃю рЃАрЃ«рЃЋрЃљ рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃћрЃЉрЃА рЃљрЃќрЃарЃў рЃљрЃдрЃљрЃа рЃљрЃЦрЃЋрЃА)
    if not s:
        return ["рЃАрЃљрЃ«рЃћрЃџрЃў рЃфрЃљрЃарЃўрЃћрЃџрЃўрЃљ Рђћ рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃЏрЃўрЃюрЃўрЃЏрЃБрЃЏ 1 рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃў рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮ (a-z)."]

    # ======================================================
    # рЃЦрЃЋрЃћрЃЏрЃЮрЃЌ рЃЋрЃљрЃЊрЃњрЃћрЃюрЃЌ рЃарЃљ рЃбрЃўрЃърЃўрЃА рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃў рЃљрЃЦрЃЋрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљрЃА
    # ======================================================

    has_digit = any(ch.isdigit() for ch in s)  # рЃљрЃЦрЃЋрЃА рЃЌрЃБ рЃљрЃарЃљ рЃЏрЃўрЃюрЃўрЃЏрЃБрЃЏ 1 рЃфрЃўрЃцрЃарЃў (0-9)
    has_upper = any("A" <= ch <= "Z" for ch in s)  # рЃљрЃЦрЃЋрЃА рЃЌрЃБ рЃљрЃарЃљ рЃЊрЃўрЃЊрЃў рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃў рЃљрЃАрЃЮ
    has_space = any(ch.isspace() for ch in s)  # рЃљрЃЦрЃЋрЃА рЃЌрЃБ рЃљрЃарЃљ space/tab/newline
    has_symbol = any((not ch.isalnum()) and (not ch.isspace()) for ch in s)
    # has_symbol: рЃљрЃЦрЃЋрЃА рЃЌрЃБ рЃљрЃарЃљ рЃАрЃърЃћрЃфрЃўрЃљрЃџрЃБрЃарЃў рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃў (рЃЏрЃљрЃњ: @ # ! - _ рЃЊрЃљ рЃљ.рЃе.), space-рЃўрЃА рЃњрЃљрЃарЃЊрЃљ

    # рЃљрЃЦрЃЋрЃА рЃЌрЃБ рЃљрЃарЃљ рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃўрЃАрЃњрЃљрЃю рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃБрЃџрЃў рЃљрЃАрЃЮ (рЃЏрЃљрЃњ: рЃЦрЃљрЃарЃЌрЃБрЃџрЃў/рЃарЃБрЃАрЃБрЃџрЃў/рЃАрЃ«рЃЋрЃљ рЃљрЃюрЃЉрЃљрЃюрЃў)
    has_non_latin_letter = any(
        ch.isalpha() and not (("a" <= ch <= "z") or ("A" <= ch <= "Z"))
        for ch in s
    )

    # ==========================================
    # рЃЎрЃЮрЃюрЃЎрЃарЃћрЃбрЃБрЃџрЃў рЃАрЃўрЃбрЃБрЃљрЃфрЃўрЃћрЃЉрЃўрЃА рЃЏрЃўрЃ«рЃћрЃЊрЃЋрЃўрЃЌ error-рЃћрЃЉрЃў
    # ==========================================

    # рЃЌрЃБ рЃЏрЃЌрЃџрЃўрЃљрЃюрЃљрЃЊ рЃфрЃўрЃцрЃарЃћрЃЉрЃўрЃљ (рЃЏрЃљрЃњ: "123")
    if s.isdigit():
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃарЃўрЃфрЃ«рЃЋрЃўрЃЌрЃў рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃљ Рђћ рЃерЃћрЃЏрЃЮрЃўрЃбрЃљрЃюрЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ string рЃърЃљрЃбрЃљрЃарЃљ рЃарЃћрЃњрЃўрЃАрЃбрЃарЃерЃў (a-z).")

    # only_symbols: рЃЌрЃБ рЃДрЃЋрЃћрЃџрЃљ рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮ рЃАрЃърЃћрЃфрЃўрЃљрЃџрЃБрЃарЃўрЃљ (рЃЏрЃљрЃњ: "@#")
    only_symbols = all((not ch.isalnum()) and (not ch.isspace()) for ch in s)
    if only_symbols:
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃў Рђћ рЃерЃћрЃЏрЃЮрЃўрЃбрЃљрЃюрЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ string рЃърЃљрЃбрЃљрЃарЃљ рЃарЃћрЃњрЃўрЃАрЃбрЃарЃерЃў (a-z).")

    # рЃЌрЃБ рЃљрЃЦрЃЋрЃА рЃњрЃљрЃЏрЃЮрЃбрЃЮрЃЋрЃћрЃЉрЃљ (рЃЏрЃљрЃњ: "ana maria")
    if has_space:
        errors.append("рЃАрЃљрЃ«рЃћрЃџрЃў рЃљрЃа рЃБрЃюрЃЊрЃљ рЃерЃћрЃўрЃфрЃљрЃЋрЃЊрЃћрЃА рЃњрЃљрЃЏрЃЮрЃбрЃЮрЃЋрЃћрЃЉрЃћрЃЉрЃА (space/tab).")

    # рЃЌрЃБ рЃљрЃЦрЃЋрЃА рЃЊрЃўрЃЊрЃў рЃљрЃАрЃЮрЃћрЃЉрЃў (рЃЏрЃљрЃњ: "Ana")
    if has_upper:
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃЊрЃўрЃЊрЃў рЃљрЃАрЃЮ(рЃћрЃЉрЃў) Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃў (a-z).")

    # рЃЌрЃБ рЃљрЃЦрЃЋрЃА рЃфрЃўрЃцрЃарЃћрЃЉрЃў рЃЊрЃљ рЃћрЃА рЃљрЃа рЃљрЃарЃўрЃА рЃерЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃљ рЃарЃЮрЃфрЃљ рЃЏрЃЌрЃџрЃўрЃљрЃюрЃљрЃЊ рЃфрЃўрЃцрЃарЃћрЃЉрЃўрЃљ (рЃЏрЃљрЃњ: "ana1")
    if has_digit and not s.isdigit():
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃфрЃўрЃцрЃарЃў(рЃћрЃЉрЃў) Рђћ рЃАрЃљрЃ«рЃћрЃџрЃерЃў рЃЊрЃљрЃерЃЋрЃћрЃЉрЃБрЃџрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃљрЃАрЃЮрЃћрЃЉрЃў (a-z).")

    # рЃЌрЃБ рЃљрЃЦрЃЋрЃА рЃАрЃърЃћрЃфрЃўрЃљрЃџрЃБрЃарЃў рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃў рЃЊрЃљ рЃћрЃА рЃљрЃа рЃљрЃарЃўрЃА only_symbols (рЃЏрЃљрЃњ: "ana_")
    if has_symbol and not only_symbols:
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃАрЃърЃћрЃфрЃўрЃљрЃџрЃБрЃарЃў рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮ(рЃћрЃЉрЃў) Рђћ рЃЊрЃљрЃерЃЋрЃћрЃЉрЃБрЃџрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃљрЃАрЃЮрЃћрЃЉрЃў (a-z).")

    # рЃЌрЃБ рЃљрЃЦрЃЋрЃА рЃАрЃ«рЃЋрЃљ рЃћрЃюрЃўрЃА рЃљрЃАрЃЮрЃћрЃЉрЃў (рЃЦрЃљрЃарЃЌрЃБрЃџрЃў/рЃарЃБрЃАрЃБрЃџрЃў рЃЊрЃљ рЃљ.рЃе.)
    if has_non_latin_letter:
        errors.append("рЃерЃћрЃЏрЃЮрЃДрЃЋрЃљрЃюрЃўрЃџрЃўрЃљ рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃўрЃАрЃњрЃљрЃю рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃБрЃџрЃў рЃљрЃАрЃЮрЃћрЃЉрЃў Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃўрЃюрЃњрЃџрЃўрЃАрЃБрЃарЃў рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃў (a-z).")

    # ==========================================================
    # рЃАрЃљрЃЉрЃЮрЃџрЃЮрЃЮ "рЃърЃљрЃбрЃћрЃарЃюрЃўрЃА" рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ: рЃБрЃюрЃЊрЃљ рЃЊрЃљрЃћрЃЏрЃЌрЃ«рЃЋрЃћрЃА рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ [a-z]+
    # ==========================================================
    if not NAME_PATTERN.match(s):
        # рЃЌрЃБ рЃБрЃЎрЃЋрЃћ рЃњрЃЋрЃљрЃЦрЃЋрЃА рЃЎрЃЮрЃюрЃЎрЃарЃћрЃбрЃБрЃџрЃў error-рЃћрЃЉрЃў, рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃўрЃЌ рЃќрЃЮрЃњрЃљрЃЊрЃА рЃљрЃдрЃљрЃа рЃЋрЃљрЃЏрЃљрЃбрЃћрЃЉрЃЌ
        if not errors:
            errors.append("рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃў рЃцрЃЮрЃарЃЏрЃљрЃбрЃў Рђћ рЃАрЃљрЃ«рЃћрЃџрЃерЃў рЃЊрЃљрЃерЃЋрЃћрЃЉрЃБрЃџрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃў рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃў (a-z).")

    return errors  # рЃЋрЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃЌ error-рЃћрЃЉрЃўрЃА рЃАрЃўрЃљрЃА (рЃфрЃљрЃарЃўрЃћрЃџрЃў рЃАрЃўрЃљ рЃюрЃўрЃерЃюрЃљрЃЋрЃА: рЃЋрЃљрЃџрЃўрЃЊрЃБрЃарЃўрЃљ)

# ==========================================
# рЃЏрЃЌрЃљрЃЋрЃљрЃарЃў рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ: UI/рЃўрЃюрЃбрЃћрЃарЃљрЃЦрЃфрЃўрЃљ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃЌрЃљрЃю
# ==========================================
def main():
    # рЃћрЃЎрЃарЃљрЃюрЃќрЃћ рЃЋрЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃЌ рЃгрЃўрЃюрЃљрЃАрЃгрЃљрЃа рЃерЃћрЃЋрЃАрЃћрЃЉрЃБрЃџ рЃЋрЃћрЃџрЃћрЃЉрЃА
    print("=== рЃарЃћрЃњрЃўрЃАрЃбрЃарЃљрЃфрЃўрЃљ ===")
    print(f"рЃћрЃџ-рЃцрЃЮрЃАрЃбрЃљ: {EMAIL}")
    print(f"рЃќрЃћрЃЊрЃЏрЃћрЃбрЃАрЃљрЃ«рЃћрЃџрЃў: {NICKNAME}")
    print(f"рЃърЃљрЃарЃЮрЃџрЃў: {PASSWORD}")
    print("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃАрЃљрЃ«рЃћрЃџрЃў (рЃџрЃљрЃЌрЃўрЃюрЃБрЃарЃў рЃърЃљрЃбрЃљрЃарЃљ рЃљрЃАрЃЮрЃћрЃЉрЃў a-z)\n")

    # рЃЋрЃљрЃбрЃарЃўрЃљрЃџрЃћрЃЉрЃЌ рЃфрЃўрЃЎрЃџрЃА рЃЏрЃљрЃюрЃљрЃЏ, рЃАрЃљрЃюрЃљрЃЏ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃАрЃгрЃЮрЃа рЃАрЃљрЃ«рЃћрЃџрЃА рЃљрЃа рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА
    while True:
        name = input("рЃАрЃљрЃ«рЃћрЃџрЃў: ")  # рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃЏрЃ«рЃарЃўрЃЊрЃљрЃю рЃћрЃарЃЌрЃљрЃЊрЃћрЃарЃЌрЃў рЃерЃћрЃАрЃљрЃДрЃЋрЃљрЃюрЃў рЃЋрЃћрЃџрЃў

        errors = validate_name(name)  # рЃЋрЃљрЃџрЃўрЃЊрЃљрЃфрЃўрЃљ: рЃЋрЃўрЃдрЃћрЃЉрЃЌ рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃўрЃА рЃАрЃўрЃљрЃА
        if not errors:
            # рЃљрЃЦ рЃБрЃЎрЃЋрЃћ рЃЋрЃўрЃфрЃўрЃЌ рЃарЃЮрЃЏ name рЃАрЃгрЃЮрЃарЃўрЃљ, рЃБрЃЉрЃарЃљрЃџрЃЮрЃЊ рЃЎрЃўрЃЊрЃћрЃЋ рЃћрЃарЃЌрЃ«рЃћрЃџ рЃЋрЃАрЃБрЃцрЃЌрЃљрЃЋрЃћрЃЉрЃЌ whitespace-рЃА
            name = name.strip()

            # рЃарЃћрЃњрЃўрЃАрЃбрЃарЃљрЃфрЃўрЃўрЃА рЃгрЃљрЃарЃЏрЃљрЃбрЃћрЃЉрЃљ рЃЊрЃљ рЃДрЃЋрЃћрЃџрЃљ рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃўрЃА рЃњрЃљрЃЏрЃЮрЃбрЃљрЃюрЃљ
            print("\nРюЁ рЃгрЃљрЃарЃЏрЃљрЃбрЃћрЃЉрЃўрЃЌ рЃЊрЃљрЃарЃћрЃњрЃўрЃАрЃбрЃарЃўрЃарЃЊрЃў! рЃерЃћрЃюрЃљрЃ«рЃБрЃџрЃў рЃЏрЃЮрЃюрЃљрЃфрЃћрЃЏрЃћрЃЉрЃў:")
            print(f"рЃћрЃџ-рЃцрЃЮрЃАрЃбрЃљ: {EMAIL}")
            print(f"рЃАрЃљрЃ«рЃћрЃџрЃў: {name}")
            print(f"рЃќрЃћрЃЊрЃЏрЃћрЃбрЃАрЃљрЃ«рЃћрЃџрЃў: {NICKNAME}")
            print(f"рЃърЃљрЃарЃЮрЃџрЃў: {PASSWORD}")
            break  # рЃЋрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃЌ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃА

        # рЃЌрЃБ рЃерЃћрЃфрЃЊрЃЮрЃЏрЃћрЃЉрЃўрЃљ Рђћ рЃДрЃЋрЃћрЃџрЃљ рЃЋрЃљрЃЕрЃЋрЃћрЃюрЃЮрЃЌ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃћрЃарЃЌрЃЊрЃарЃЮрЃБрЃџрЃљрЃЊ
        print("\nрЃерЃћрЃДрЃЋрЃљрЃюрЃљ рЃљрЃарЃљрЃАрЃгрЃЮрЃарЃўрЃљ. рЃЊрЃљрЃцрЃўрЃЦрЃАрЃўрЃарЃЊрЃљ:")
        for i, msg in enumerate(errors, 1):
            print(f"{i}) {msg}")
        print("рЃАрЃфрЃљрЃЊрЃћ рЃЎрЃўрЃЊрЃћрЃЋ рЃћрЃарЃЌрЃ«рЃћрЃџ.\n")

# ==========================================
# рЃАрЃЎрЃарЃўрЃърЃбрЃўрЃА рЃњрЃљрЃерЃЋрЃћрЃЉрЃўрЃА рЃАрЃбрЃљрЃюрЃЊрЃљрЃарЃбрЃБрЃџрЃў entry-point
# ==========================================
if __name__ == "__main__":
    main()  # main() рЃњрЃљрЃћрЃерЃЋрЃћрЃЉрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃЏрЃљрЃерЃўрЃю, рЃарЃЮрЃфрЃљ рЃцрЃљрЃўрЃџрЃА рЃБрЃерЃБрЃљрЃџрЃЮрЃЊ рЃЋрЃБрЃерЃЋрЃћрЃЉрЃЌ
