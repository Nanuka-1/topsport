# рЃЊрЃљрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃўрЃА рЃЊрЃљрЃЌрЃЋрЃџрЃљ
# рЃўрЃдрЃћрЃЉрЃА рЃЎрЃЮрЃЏрЃърЃўрЃБрЃбрЃћрЃарЃўрЃА рЃЊрЃљ рЃЏрЃЮрЃЌрЃљрЃЏрЃљрЃерЃўрЃА рЃАрЃўрЃћрЃЉрЃА
# рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃарЃљрЃЏрЃЊрЃћрЃюрЃў рЃарЃўрЃфрЃ«рЃЋрЃў рЃЊрЃљрЃћрЃЏрЃЌрЃ«рЃЋрЃљ
# ================================
def count_matches(computer, player):
    return len(set(computer) & set(player))


# ================================
# рЃЏрЃЮрЃњрЃћрЃЉрЃўрЃА рЃњрЃљрЃЏрЃЮрЃЌрЃЋрЃџрЃљ
# matches -> рЃЊрЃљрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃўрЃА рЃарЃљрЃЮрЃЊрЃћрЃюрЃЮрЃЉрЃљ
# ================================
def calculate_prize(matches):
    multiplier = PRIZES.get(matches, 0)  # рЃЌрЃБ рЃљрЃа рЃљрЃарЃАрЃћрЃЉрЃЮрЃЉрЃА, 0
    return int(JACKPOT * multiplier)


# ================================
# рЃерЃћрЃЊрЃћрЃњрЃўрЃА рЃЕрЃЋрЃћрЃюрЃћрЃЉрЃљ рЃћрЃЎрЃарЃљрЃюрЃќрЃћ
# рЃърЃљрЃАрЃБрЃ«рЃўрЃАрЃЏрЃњрЃћрЃЉрЃћрЃџрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ UI-рЃќрЃћ
# ================================
def show_result(computer, player, matches, prize):
    print("\n  рЃџрЃљрЃбрЃљрЃарЃўрЃўрЃА рЃерЃћрЃЊрЃћрЃњрЃў ")
    print("рЃЎрЃЮрЃЏрЃърЃўрЃБрЃбрЃћрЃарЃўрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў:", sorted(computer))
    print("рЃерЃћрЃюрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў:", sorted(player))
    print("рЃЊрЃљрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃў:", matches)
    print("рЃЏрЃЮрЃњрЃћрЃЉрЃљ:", prize)


# ================================
# рЃерЃћрЃЊрЃћрЃњрЃўрЃА рЃЕрЃљрЃгрЃћрЃарЃљ рЃџрЃЮрЃњ рЃцрЃљрЃўрЃџрЃерЃў
# рЃАрЃљрЃГрЃўрЃарЃЮрЃљ рЃўрЃАрЃбрЃЮрЃарЃўрЃўрЃА рЃЊрЃљ рЃљрЃюрЃљрЃџрЃўрЃќрЃўрЃАрЃЌрЃЋрЃўрЃА
# ================================
def log_result(computer, player, matches, prize):
    logging.info(
        f"computer={sorted(computer)} | "
        f"player={sorted(player)} | "
        f"matches={matches} | "
        f"jackpot={JACKPOT} | "
        f"prize={prize}"
    )


# ================================
# рЃЏрЃЌрЃљрЃЋрЃљрЃарЃў рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ
# рЃљрЃћрЃарЃЌрЃўрЃљрЃюрЃћрЃЉрЃА рЃЏрЃЌрЃћрЃџ рЃџрЃЮрЃњрЃўрЃЎрЃљрЃА рЃћрЃарЃЌ рЃюрЃљрЃЎрЃљрЃЊрЃљрЃЊ
# ================================
def main():
    print(" рЃЎрЃћрЃЌрЃўрЃџрЃў рЃўрЃДрЃЮрЃА рЃерЃћрЃюрЃў рЃЏрЃЮрЃЉрЃарЃФрЃљрЃюрЃћрЃЉрЃљ рЃџрЃљрЃбрЃљрЃарЃўрЃљрЃерЃў ")

    # рЃЎрЃЮрЃЏрЃърЃўрЃБрЃбрЃћрЃарЃўрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃњрЃћрЃюрЃћрЃарЃљрЃфрЃўрЃљ
    computer_numbers = generate_computer_numbers()

    # рЃЏрЃЮрЃЌрЃљрЃЏрЃљрЃерЃўрЃА рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљ
    player_numbers = get_player_numbers()

    # рЃЊрЃљрЃЏрЃЌрЃ«рЃЋрЃћрЃЋрЃћрЃЉрЃўрЃА рЃЊрЃљрЃЌрЃЋрЃџрЃљ
    matches = count_matches(computer_numbers, player_numbers)

    # рЃЏрЃЮрЃњрЃћрЃЉрЃўрЃА рЃњрЃљрЃЏрЃЮрЃЌрЃЋрЃџрЃљ
    prize = calculate_prize(matches)

    # рЃерЃћрЃЊрЃћрЃњрЃўрЃА рЃЕрЃЋрЃћрЃюрЃћрЃЉрЃљ
    show_result(computer_numbers, player_numbers, matches, prize)

    # рЃерЃћрЃЊрЃћрЃњрЃўрЃА рЃџрЃЮрЃњрЃўрЃарЃћрЃЉрЃљ
    log_result(computer_numbers, player_numbers, matches, prize)


# ================================
# рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃА рЃњрЃљрЃерЃЋрЃћрЃЉрЃўрЃА рЃгрЃћрЃарЃбрЃўрЃџрЃў
# рЃАрЃљрЃГрЃўрЃарЃЮрЃљ, рЃарЃЮрЃЏ main рЃњрЃљрЃћрЃерЃЋрЃљрЃА рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃърЃўрЃарЃЊрЃљрЃърЃўрЃа рЃњрЃљрЃерЃЋрЃћрЃЉрЃўрЃАрЃљрЃА
# ================================
if __name__ == "__main__":
    main()


