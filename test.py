# ================================
# рЃАрЃљрЃгрЃДрЃўрЃАрЃў рЃЉрЃљрЃџрЃљрЃюрЃАрЃў (рЃњрЃџрЃЮрЃЉрЃљрЃџрЃБрЃарЃў рЃфрЃЋрЃџрЃљрЃЊрЃў)
# ================================
balance = 500  # рЃџрЃљрЃарЃў


# ================================
# рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃЕрЃЋрЃћрЃюрЃћрЃЉрЃўрЃА рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ
# рЃърЃљрЃАрЃБрЃ«рЃўрЃАрЃЏрЃњрЃћрЃЉрЃћрЃџрЃўрЃљ рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃўрЃюрЃцрЃЮрЃарЃЏрЃљрЃфрЃўрЃўрЃА рЃЕрЃЋрЃћрЃюрЃћрЃЉрЃљрЃќрЃћ
# ================================
def show_balance():
    print(f"рЃЌрЃЦрЃЋрЃћрЃюрЃў рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃљ: {balance} рЃџрЃљрЃарЃў")


# ================================
# рЃЌрЃљрЃюрЃ«рЃўрЃА рЃерЃћрЃбрЃљрЃюрЃўрЃА рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ
# amount РђЊ рЃерЃћрЃАрЃљрЃбрЃљрЃюрЃў рЃЌрЃљрЃюрЃ«рЃљ
# ================================
def deposit(amount):
    global balance  # рЃЋрЃўрЃДрЃћрЃюрЃћрЃЉрЃЌ рЃњрЃџрЃЮрЃЉрЃљрЃџрЃБрЃа balance рЃфрЃЋрЃџрЃљрЃЊрЃА

    # рЃерЃћрЃќрЃдрЃБрЃЊрЃЋрЃљ: рЃћрЃарЃЌрЃ»рЃћрЃарЃљрЃЊрЃљрЃЊ рЃЏрЃљрЃЦрЃАрЃўрЃЏрЃБрЃЏ 1000 рЃџрЃљрЃарЃў
    if amount > 1000:
        print("рЃћрЃарЃЌрЃ»рЃћрЃарЃљрЃЊрЃљрЃЊ 1000 рЃџрЃљрЃарЃќрЃћ рЃЏрЃћрЃбрЃўрЃА рЃерЃћрЃбрЃљрЃюрЃљ рЃљрЃа рЃерЃћрЃўрЃФрЃџрЃћрЃЉрЃљ ")
        return  # рЃцрЃБрЃюрЃЦрЃфрЃўрЃўрЃА рЃЊрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃљ

    # рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃњрЃљрЃќрЃарЃЊрЃљ
    balance += amount

    # рЃџрЃЮрЃњ рЃцрЃљрЃўрЃџрЃерЃў рЃЕрЃљрЃгрЃћрЃарЃљ
    logging.info(f"рЃерЃћрЃбрЃљрЃюрЃљ: {amount} рЃџрЃљрЃарЃў")

    # рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃЌрЃЋрЃўрЃА рЃерЃћрЃбрЃДрЃЮрЃЉрЃўрЃюрЃћрЃЉрЃљ
    print(f"рЃерЃћрЃбрЃљрЃюрЃўрЃџрЃўрЃљ {amount} рЃџрЃљрЃарЃў")


# ================================
# рЃЌрЃљрЃюрЃ«рЃўрЃА рЃњрЃљрЃбрЃљрЃюрЃўрЃА рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ
# amount РђЊ рЃњрЃљрЃАрЃљрЃбрЃљрЃюрЃў рЃЌрЃљрЃюрЃ«рЃљ
# ================================
def withdraw(amount):
    global balance  # рЃњрЃџрЃЮрЃЉрЃљрЃџрЃБрЃарЃў рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃњрЃљрЃЏрЃЮрЃДрЃћрЃюрЃћрЃЉрЃљ

    # рЃерЃћрЃЏрЃЮрЃгрЃЏрЃћрЃЉрЃљ: рЃАрЃљрЃЎрЃЏрЃљрЃарЃўрЃАрЃўрЃљ рЃЌрЃБ рЃљрЃарЃљ рЃЌрЃљрЃюрЃ«рЃљ
    if amount > balance:
        print("рЃљрЃюрЃњрЃљрЃарЃўрЃерЃќрЃћ рЃљрЃа рЃљрЃарЃўрЃА рЃАрЃљрЃЎрЃЏрЃљрЃарЃўрЃАрЃў рЃЌрЃљрЃюрЃ«рЃљ ")
        return  # рЃцрЃБрЃюрЃЦрЃфрЃўрЃўрЃА рЃЊрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃљ

    # рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃерЃћрЃЏрЃфрЃўрЃарЃћрЃЉрЃљ
    balance -= amount

    # рЃџрЃЮрЃњ рЃцрЃљрЃўрЃџрЃерЃў рЃЕрЃљрЃгрЃћрЃарЃљ
    logging.info(f"рЃњрЃљрЃбрЃљрЃюрЃљ: {amount} рЃџрЃљрЃарЃў")

    # рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃАрЃЌрЃЋрЃўрЃА рЃерЃћрЃбрЃДрЃЮрЃЉрЃўрЃюрЃћрЃЉрЃљ
    print(f"рЃњрЃљрЃбрЃљрЃюрЃўрЃџрЃўрЃљ {amount} рЃџрЃљрЃарЃў")


# ================================
# рЃЏрЃЌрЃљрЃЋрЃљрЃарЃў рЃЏрЃћрЃюрЃўрЃБрЃА рЃфрЃўрЃЎрЃџрЃў
# рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ рЃЏрЃБрЃерЃљрЃЮрЃЉрЃА рЃЏрЃљрЃюрЃљрЃЏ, рЃАрЃљрЃюрЃљрЃЏ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃљрЃа рЃљрЃўрЃарЃЕрЃћрЃЋрЃА рЃњрЃљрЃЏрЃЮрЃАрЃЋрЃџрЃљрЃА
# ================================
while True:
    print("\n--- ATM Machine ---")
    print("1. рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃюрЃљрЃ«рЃЋрЃљ")
    print("2. рЃЌрЃљрЃюрЃ«рЃўрЃА рЃерЃћрЃбрЃљрЃюрЃљ")
    print("3. рЃЌрЃљрЃюрЃ«рЃўрЃА рЃњрЃљрЃбрЃљрЃюрЃљ")
    print("4. рЃњрЃљрЃЏрЃЮрЃАрЃЋрЃџрЃљ")

    # рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃџрЃўрЃА рЃљрЃарЃЕрЃћрЃЋрЃљрЃюрЃў
    choice = input("рЃљрЃўрЃарЃЕрЃўрЃћ рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃљ (1-4): ")

    # рЃЉрЃљрЃџрЃљрЃюрЃАрЃўрЃА рЃюрЃљрЃ«рЃЋрЃљ
    if choice == "1":
        show_balance()

    # рЃЌрЃљрЃюрЃ«рЃўрЃА рЃерЃћрЃбрЃљрЃюрЃљ
    elif choice == "2":
        amount = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃерЃћрЃАрЃљрЃбрЃљрЃюрЃў рЃЌрЃљрЃюрЃ«рЃљ (рЃџрЃљрЃарЃў): "))
        deposit(amount)

    # рЃЌрЃљрЃюрЃ«рЃўрЃА рЃњрЃљрЃбрЃљрЃюрЃљ
    elif choice == "3":
        amount = int(input("рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃћ рЃњрЃљрЃАрЃљрЃбрЃљрЃюрЃў рЃЌрЃљрЃюрЃ«рЃљ (рЃџрЃљрЃарЃў): "))
        withdraw(amount)

    # рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃЊрЃљрЃю рЃњрЃљрЃЏрЃЮрЃАрЃЋрЃџрЃљ
    elif choice == "4":
        print("рЃЏрЃљрЃЊрЃџрЃЮрЃЉрЃљ рЃАрЃљрЃарЃњрЃћрЃЉрЃџрЃЮрЃЉрЃўрЃАрЃЌрЃЋрЃўрЃА ­ЪЉІ")
        break  # рЃфрЃўрЃЎрЃџрЃўрЃА рЃЊрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃљ рЃЊрЃљ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃўрЃА рЃњрЃљрЃЕрЃћрЃарЃћрЃЉрЃљ

